#include <iostream>
using namespace std;

int main() {
    int a = 10;       // 整数类型
    short b = 5;      // 短整数类型
    long c = 100000;  // 长整数类型
    long long d = 1000000000;  // 更长的整数类型

    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "c = " << c << endl;
    cout << "d = " << d << endl;

    return 0;
}

